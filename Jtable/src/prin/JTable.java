    void consultar (){
        String sql="Select * from registrodatos";
         
        
        try{
            conet = con1.getConnection();
            st = (Statement) conet.createStatement();
            rs = (ResultSet) st.executeQuery(sql);
            Object[] registrodatos = new Object [4];
            modelo = (DefaultTableModel)Tabla.getModel();
            while (rs.next()){
                registrodatos [0] = rs.getString("Telefono");
                registrodatos [1] = rs.getString("Nombre");
                registrodatos [2] = rs.getString("Dirección");
                registrodatos [3] = rs.getString("Email");
                
                modelo.addRow(registrodatos);
                
            }
            Tabla.setModel(modelo);
            
        } catch(Exception e){
        
        }
    
    }
    
    void Agregar(){
        String tel = txtTELEFONO.getText();
        String nom =txtNOMBRE.getText();
        String Dicc =txtDIRECCION.getText();
        String Mail = txtEMAIL.getText();
        
        try{
            if (tel.equals("") || nom.equals("") || Dicc.equals("") || Mail.equals("")) {
                JOptionPane.showConfirmDialog(null, "Ingresar TODOS los DATOS");
                limpiarTabla();
            } else {
                String sql = "insert into registrodatos(Telefono,Nombre,Dirección,Email) values ('"+ tel +"','"+nom+"','"+Dicc+"','"+Mail+"')";
                conet = con1.getConnection();
                st = (Statement) conet.createStatement();
                st.executeUpdate(sql);
                    JOptionPane.showConfirmDialog(null, "Nuevo Registro AGREGADO");
                    limpiarTabla();
            }
        } catch (Exception e){
        
        }
            
    }
    
    
    void limpiarTabla(){
        for(int i = 0; i <= Tabla.getRowCount();i++){
            modelo.removeRow(i);
            i = i-1;
        }
    }
    
    void Eliminar(){
    
        int fila = Tabla.getSelectedRow();
        try{
            if (fila<0){
                JOptionPane.showConfirmDialog(null, "Registro No SELECCIONADO");
                limpiarTabla();
            } else {
                String sql="delete from registrodatos where Telefono="+idc;
                conet = con1.getConnection();
                st = (Statement) conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "REGISTRO Eliminado");
                limpiarTabla();
            }
        } catch(Exception e) {
        
        }
    }
    
