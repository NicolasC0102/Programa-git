    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        int fila = Tabla.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(null, "NO sea SELECIONADO una FILA");
        } else {
            idc = (String) Tabla.getValueAt(fila, 0);
            String nom = (String) Tabla.getValueAt(fila, 1);
            String Dicc = (String) Tabla.getValueAt(fila, 2);
            String Mail = (String) Tabla.getValueAt(fila, 3);
            
            txtTELEFONO.setText(""+ idc);
            txtNOMBRE.setText(nom);
            txtDIRECCION.setText(Dicc);
            txtEMAIL.setText(Mail);
            
        }
    }//GEN-LAST:event_TablaMouseClicked

